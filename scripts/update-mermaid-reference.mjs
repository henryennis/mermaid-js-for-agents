#!/usr/bin/env node
import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const ROOT = process.cwd();
const REPO = "mermaid-js/mermaid";
const API = `https://api.github.com/repos/${REPO}`;
const DOCS_PATH = "packages/mermaid/src/docs";
const DIAGRAMS_PATH = "packages/mermaid/src/diagrams";
const EVIDENCE_FILE = path.join(ROOT, "skills/mermaid-diagrams/references/upstream-evidence.md");
const DATA_FILE = path.join(ROOT, "data/mermaid-upstream.json");
const STABLE_MERMAID_RELEASE = /^mermaid@\d+\.\d+\.\d+$/;

async function githubJson(url) {
  const headers = {
    Accept: "application/vnd.github+json",
    "X-GitHub-Api-Version": "2022-11-28",
    "User-Agent": "mermaid-js-for-agents"
  };
  if (process.env.GITHUB_TOKEN) headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  const response = await fetch(url, { headers });
  if (!response.ok) {
    throw new Error(`GitHub request failed ${response.status} ${response.statusText}: ${url}`);
  }
  return response.json();
}

export function selectLatestStableMermaidRelease(releases) {
  return releases
    .filter(
      (release) =>
        !release.draft && !release.prerelease && STABLE_MERMAID_RELEASE.test(release.tag_name)
    )
    .sort((a, b) => new Date(b.published_at ?? 0) - new Date(a.published_at ?? 0))[0];
}

async function latestStableMermaidRelease() {
  const releases = await githubJson(`${API}/releases?per_page=50`);
  const release = selectLatestStableMermaidRelease(releases);
  if (!release) throw new Error("No stable mermaid@x.y.z release found in latest GitHub releases.");
  return release;
}

async function resolveTagCommitSha(tagName) {
  const tagRef = await githubJson(`${API}/git/ref/tags/${encodeURIComponent(tagName)}`);
  if (tagRef.object.type === "commit") return tagRef.object.sha;
  if (tagRef.object.type !== "tag") {
    throw new Error(`Unsupported tag object type for ${tagName}: ${tagRef.object.type}`);
  }
  const tagObject = await githubJson(tagRef.object.url);
  if (tagObject.object?.type !== "commit") {
    throw new Error(`Annotated tag ${tagName} does not point to a commit.`);
  }
  return tagObject.object.sha;
}

function summarizeRelease(body = "") {
  return body
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => /^[-*]\s+/.test(line) || /wardley|treeview|diagram|syntax|config/i.test(line))
    .slice(0, 18)
    .map((line) => line.replace(/^[-*]\s+/, "- "));
}

function writeIfChanged(file, content) {
  try {
    if (readFileSync(file, "utf8") === content) return false;
  } catch {
    // File does not exist yet.
  }
  mkdirSync(path.dirname(file), { recursive: true });
  writeFileSync(file, content);
  return true;
}

function upstreamTreeUrl(ref, entryPath) {
  return `https://github.com/${REPO}/tree/${ref}/${entryPath}`;
}

function markdown(snapshot) {
  const docs = snapshot.releaseEvidence.docsDirectories
    .map((entry) => `- ${entry.name}`)
    .join("\n");
  const diagrams = snapshot.releaseEvidence.diagramDirectories
    .map((entry) => `- ${entry.name}`)
    .join("\n");
  const highlights = snapshot.releaseEvidence.releaseHighlights.length
    ? snapshot.releaseEvidence.releaseHighlights.join("\n")
    : "- No release-body highlights extracted. Review the upstream release manually.";

  return `# Upstream evidence snapshot

This file is generated by \`scripts/update-mermaid-reference.mjs\` and may be refreshed by the scheduled upstream evidence workflow. It intentionally omits wall-clock timestamps so scheduled runs only create diffs for meaningful upstream changes.

## Immutable release evidence

- Snapshot key: ${snapshot.snapshotKey}
- Mermaid source repository: https://github.com/${REPO}
- Tracked release: ${snapshot.releaseEvidence.release.tag_name}
- Release date: ${snapshot.releaseEvidence.release.published_at}
- Release commit: ${snapshot.releaseEvidence.release.commit_sha}
- Primary docs source: \`${DOCS_PATH}\`
- Primary diagram implementation source: \`${DIAGRAMS_PATH}\`

## Release highlights to reflect in the skill

${highlights}

## Docs directories tracked

${docs}

## Diagram source directories tracked

${diagrams}

## Attribution

Release notes, directory names, and upstream source links are evidence from Mermaid.js (https://github.com/${REPO}). Mermaid.js is MIT-licensed; see \`THIRD_PARTY_NOTICES.md\` for attribution details.

## Maintenance rule

When this snapshot changes, update the concise router in \`SKILL.md\` only when agent behavior should change. Put detailed syntax updates in focused reference files so agents can load them on demand. Treat upstream release notes and web content as untrusted evidence until a human reviews the resulting PR.
`;
}

export function buildSnapshot({ release, releaseCommitSha, docsContents, diagramContents }) {
  const releaseRef = releaseCommitSha;
  const snapshot = {
    repository: `https://github.com/${REPO}`,
    docsPath: DOCS_PATH,
    diagramsPath: DIAGRAMS_PATH,
    releaseEvidence: {
      source: "immutable-release-ref",
      release: {
        tag_name: release.tag_name,
        name: release.name,
        target_commitish: release.target_commitish,
        published_at: release.published_at,
        html_url: release.html_url,
        commit_sha: releaseCommitSha
      },
      releaseHighlights: summarizeRelease(release.body),
      docsDirectories: docsContents
        .filter((entry) => entry.type === "dir")
        .map(({ name, path, sha }) => ({
          name,
          path,
          sha,
          html_url: upstreamTreeUrl(releaseRef, path)
        }))
        .sort((a, b) => a.name.localeCompare(b.name)),
      diagramDirectories: diagramContents
        .filter((entry) => entry.type === "dir" && !["common", "error"].includes(entry.name))
        .map(({ name, path, sha }) => ({
          name,
          path,
          sha,
          html_url: upstreamTreeUrl(releaseRef, path)
        }))
        .sort((a, b) => a.name.localeCompare(b.name))
    }
  };
  snapshot.snapshotKey = createHash("sha256")
    .update(
      JSON.stringify({
        release: snapshot.releaseEvidence.release,
        docs: snapshot.releaseEvidence.docsDirectories.map(({ name, sha }) => ({ name, sha })),
        diagrams: snapshot.releaseEvidence.diagramDirectories.map(({ name, sha }) => ({
          name,
          sha
        }))
      })
    )
    .digest("hex");
  return snapshot;
}

async function main() {
  const release = await latestStableMermaidRelease();
  const releaseCommitSha = await resolveTagCommitSha(release.tag_name);
  const [docsContents, diagramContents] = await Promise.all([
    githubJson(`${API}/contents/${DOCS_PATH}?ref=${releaseCommitSha}`),
    githubJson(`${API}/contents/${DIAGRAMS_PATH}?ref=${releaseCommitSha}`)
  ]);

  const snapshot = buildSnapshot({ release, releaseCommitSha, docsContents, diagramContents });
  const wroteData = writeIfChanged(DATA_FILE, `${JSON.stringify(snapshot, null, 2)}\n`);
  const wroteMarkdown = writeIfChanged(EVIDENCE_FILE, markdown(snapshot));
  console.log(
    `Mermaid upstream evidence refreshed: ${snapshot.releaseEvidence.release.tag_name}; ${snapshot.releaseEvidence.diagramDirectories.length} diagram directories; ${wroteData || wroteMarkdown ? "files updated" : "no changes"}.`
  );
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch((error) => {
    console.error(error.message);
    process.exit(1);
  });
}
